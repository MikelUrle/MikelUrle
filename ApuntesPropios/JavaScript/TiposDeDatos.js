// Tipos de datos

// 1. 'Number':

// Incluye enteros y números de punto flotante.

let edad = 30;
let precio = 19.99;

// 2. 'String':

// Cadena de caracteres, se puede definir con comillas simples ('), comillas dobles (") o backticks (`) para templates de cadena.

let nombre = "Juan";
let saludo = `Hola, ${nombre}`;

// 2. 'Boolean':

// true o false.

let esMayorDeEdad = true;

// 3. 'Object':

// Colección de propiedades y métodos.

let persona = {
    nombre: "Juan",
    edad: 30
};

// 4. 'Array':

// Lista ordenada de elementos.

let numeros = [1, 2, 3, 4, 5];

// 5. 'Function':

// Bloque de código reutilizable.

function saludar() {
    console.log("Hola");
}

// 6. 'Null':

// Representa la ausencia intencional de cualquier valor.

let coche = null;

// 7. 'Undefined':

// Una variable que ha sido declarada pero no inicializada.

let valorIndefinido;

// 8. 'Undefined':

// Una variable que ha sido declarada pero no inicializada.

let valorIndefinido1;

// 9. 'Symbol':

// Tipo de dato único e inmutable.

let simbolo = Symbol("descripcion");

// 10. 'BigInt':

// Para representar números enteros de gran tamaño.

let numeroGrande = BigInt(1234567890123456789012345678901234567890n);

